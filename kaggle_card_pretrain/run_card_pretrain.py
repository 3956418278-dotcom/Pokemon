def run(cmd: list[str], source_dir: Path, work_dir: Path) -> None:
    print("+", " ".join(cmd), flush=True)
    env = os.environ.copy()
    env["PYTHONPATH"] = str(source_dir) + os.pathsep + env.get("PYTHONPATH", "")
    subprocess.run(cmd, check=True, cwd=work_dir, env=env)


